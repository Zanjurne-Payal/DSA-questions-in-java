package methods;

public class std_liabrary_methods {
    public static void main(String[] args) {
        System.out.println(Math.sqrt(16));
        System.out.println(Math.sqrt(90));

        System.out.println(Math.max(90,8));
        System.out.println(Math.min(90,56));

        System.out.println(Math.ceil(9.7));
        System.out.println(Math.floor(9.7));

        System.out.println(Math.log(90));
    }
}
